package cn.kitrst.blog.domain;

public class Tag {

	private int tagid;
	
	private String name;

	public int getTagid() {
		return tagid;
	}

	public void setTagid(int id) {
		this.tagid = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
